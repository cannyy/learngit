package com.canny.bts;
//Record类数据的存储
public class Record {
	private long mId;
	private String mPt;
	private String mX;
	private String mY;
	private String mZ;
	public long getmId() {
		return mId;
	}
	public void setmId(long mId) {
		this.mId = mId;
	}
	public String getmPt() {
		return mPt;
	}
	public void setmPt(String mPt) {
		this.mPt = mPt;
	}
	public String getmX() {
		return mX;
	}
	public void setmX(String mX) {
		this.mX = mX;
	}
	public String getmY() {
		return mY;
	}
	public void setmY(String mY) {
		this.mY = mY;
	}
	public String getmZ() {
		return mZ;
	}
	public void setmZ(String mZ) {
		this.mZ = mZ;
	}
}
